# Desafio de Código Ruby: Impressão de Números com Condição

# Objetivo: Escreva um programa em Ruby que imprima todos os números de 1 a 99 usando um loop while.

# Instruções:

# 1 Inicialize uma variável num com o valor 1.
# 2 Use um loop while para imprimir os números a partir de 1 e menores que 100.
# 4 Dentro do loop, incremente o valor da variável num a cada iteração.

num = 1

while num < 100
    puts num
    num += 1
end