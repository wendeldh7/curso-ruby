# Desafio de Código Ruby: Verificação de Valor
# Objetivo: Escreva um programa em Ruby que receba um valor inteiro e verifique se ele é maior que 15. Caso contrário, informe que o valor é menor ou igual a 15.

# Instruções:

# 1 Escreva uma variáveis x é dentro da variáveis coloque um número inteiro ex: (1,2,3).
# 2 Use a estrutura unless para verificar se o número é maior que 15.
# 3 Imprima "O número é menor ou igual a 15" se a condição do unless for verdadeira.
# 4 Caso contrário, imprima "O número é maior que 15".

x = 10
unless x > 15 # > A menos que x seja maior que 15
    puts "x é maior ou igual a 15"
else
    puts "x é maior que 15"
end